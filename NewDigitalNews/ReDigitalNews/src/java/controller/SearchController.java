    public static boolean allSpace(String value) {
        for (int i = 0; i < value.length(); i++) {
            if (!Character.isSpaceChar(value.charAt(i))) {
                return false;
            }
        }
        return true;
    }

    public static boolean checkEmpty(String value) {
        if (!(value == null)) {
            if (!value.isEmpty()) {
                return allSpace(value);
            }
        }

        return true;
    }

